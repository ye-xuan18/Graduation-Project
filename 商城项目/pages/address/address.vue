<template>
	<view class="content b-t">
		<empty v-if="addressList.length === 0"></empty>
		<view class="list b-b" v-for="(item, index) in addressList" :key="index" @click="checkAddress(item)">
			<view class="wrapper">
				<view class="address-box">
					<text v-if="item.default" class="tag">默认</text>
					<text class="address">{{item.street}}</text>
				</view>
				<view class="u-box">
					<text class="name">{{item.name}}</text>
					<text class="mobile">{{item.telephone}}</text>
				</view>
			</view>

			<uni-icons type="compose" size="25" @click="addAddress('edit', item)"></uni-icons>
		</view>

		<button class="add-btn" @click="addAddress('add')">新增地址</button>
	</view>
</template>

<script>
	import empty from "@/components/empty";
	import {
		mapState,
		mapMutations
	} from 'vuex';
	export default {
		components: {
			empty
		},
		data() {
			return {
				source: 0,
				addressList: []
			}
		},
		onLoad(option) {
			console.log(option.source);
			this.source = option.source;
			this.inquiryAddress();
		},
		computed: {
			...mapState(['hasLogin', 'userInfo'])
		},
		methods: {
			change(open) {
				console.log('当前开启状态：' + open)
			},
			//选择地址
			checkAddress(item) {
				if (this.source == 1) {
					//this.$api.prePage()获取上一页实例，在App.vue定义
					this.$api.prePage().addressData = item;
					uni.navigateBack()
				}
			},
			addAddress(type, item) {
				uni.navigateTo({
					url: `/pages/address/addressManage?type=${type}&data=${JSON.stringify(item)}`
				})
			},
			//添加或修改成功之后回调
			refreshList() {
				this.inquiryAddress();
			},
			//查询收货地址
			inquiryAddress() {
				this.$api.request.userShip({
					userDTO: {
						userUuid: this.userInfo.userUuid
					}
				}, res => {
					if (res.body.status.statusCode === '0') {
						this.addressList = res.body.data.addresses;
					} else {
						this.$api.msg(res.body.status.errorDesc);
					}
				});
			}
		}
	}
</script>

